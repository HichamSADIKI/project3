"""Génération du relevé propriétaire en PDF (WeasyPrint) + stockage MinIO.

- `build_statement_html()` : gabarit HTML PUR (testable sans WeasyPrint), bilingue
  EN/AR — lignes Revenu brut / Charges / Commission de gestion / Net à reverser.
- `generate_and_store_statement_pdf()` : récupère le relevé (tenant), rend le PDF
  (réutilise `render_pdf` de finance.invoice), le stocke dans MinIO
  (owner_statements/{company}/{party}/{period}.pdf) et renvoie une URL présignée.

Réutilise la chaîne PDF existante (finance.invoice.render_pdf) — pas de duplication
du moteur WeasyPrint.
"""

from __future__ import annotations

# ruff: noqa: E501  (gabarit HTML/CSS : lignes longues volontaires)
import html
import uuid
from decimal import Decimal

from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from app.core import storage
from app.models.company import Company
from app.routers.finance.invoice import render_pdf
from app.routers.owner_statements.service import get_statement, statement_period_label


class StatementError(ValueError):
    """Erreur métier relevé propriétaire (code lisible pour le router)."""

    def __init__(self, code: str) -> None:
        self.code = code
        super().__init__(code)


def _money(value: Decimal | str | float) -> str:
    """Format AED, chiffres latins, 2 décimales."""
    return f"AED {Decimal(str(value)).quantize(Decimal('0.01')):,}"


