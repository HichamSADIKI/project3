async def generate_and_store_statement_pdf(
    db: AsyncSession, company_id: uuid.UUID, party_id: uuid.UUID, statement_id: uuid.UUID
) -> str:
    """Génère + stocke le PDF d'un relevé du tenant. Renvoie une URL présignée.

    Lève StatementError (statement_not_found / storage_not_configured)."""
    statement = await get_statement(db, company_id, statement_id)
    if statement is None or statement.owner_party_id != party_id:
        raise StatementError("statement_not_found")
    if not storage.is_configured():
        raise StatementError("storage_not_configured")

    company = (
        await db.execute(select(Company).where(Company.id == company_id))
    ).scalar_one_or_none()
    period_label = statement_period_label(statement.period_year, statement.period_month)
    reference = f"OS-{period_label}-{str(statement.id)[:8]}"

    statement_html = build_statement_html(
        reference=reference,
        company_name=company.name if company else "Infinity",
        period_label=period_label,
        owner_name=str(party_id),
        gross_revenue=statement.gross_revenue_aed,
        expenses=statement.expenses_aed,
        commission=statement.commission_aed,
        net_payout=statement.net_payout_aed,
        currency=statement.currency,
    )
    pdf = render_pdf(statement_html)
    object_key = f"owner_statements/{company_id}/{party_id}/{period_label}.pdf"
    await storage.upload_bytes(object_key, pdf, "application/pdf")
    url = await storage.presigned_url(object_key)
    if not url:
        raise StatementError("storage_not_configured")
    return url
